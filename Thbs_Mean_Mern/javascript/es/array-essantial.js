//push method
let msg =['Hi' , 'Bharath'];
msg.push('pandhi' , 'kukka' , 'nakka' , 'dhunnapothu'); //added items end of the array
 console.log (`${msg}`); 

 // pop method 
msg.pop(); //removed items end of the array
console.log(`${msg}`); 

//unshift method

msg.unshift('Chinnu','Bangaram'); //added items starting of the array
console.log(`${msg}`);

//shift method 

msg.shift();    //removed items starting of the array
console.log(`${msg}`);